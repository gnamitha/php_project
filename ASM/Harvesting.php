     <td colspan="2" rowspan="2"><center><h2>Harvesting Techniques</h2></center>
    <h3> <p>Harvesting is the act or process of gathering a ripe crop from the fields. Reaping is the cutting of grain or pulse for harvest, typically using a scythe, sickle, or reaper.[1] On smaller farms with minimal mechanization, harvesting is the most labor-intensive activity of the growing season. On large mechanized farms, harvesting utilizes the most expensive and sophisticated farm machinery, such as the combine harvester. The term "harvesting" in general usage may include immediate postharvest handling, including cleaning, sorting, packing, and cooling.</p>

<p>The completion of harvesting marks the end of the growing season, or the growing cycle for a particular crop, and the social importance of this event makes it the focus of seasonal celebrations such as harvest festivals, found in many religions.
     </p>
     Harvesting can be classified into two broad categories, viz. hand harvesting and mechanical harvesting.

<ol><li>Hand/Manual Harvesting: Also termed as hand reaping, it includes plucking the ears of grain directly by hand, cutting the grain stalks with a sickle, cutting them with a scythe, or with a later type of scythe called a cradle.</li>
<li>Mechanical Harvesting: Harvesting done by employing a mechanical reaper or reaping machine is termed as mechanical harvesting.</li></ol>
<p>Harvesting machinery or equipment is a mechanical device used for harvesting. There are several types of harvesting machines which are generally classified by crop. Reapers are used for cutting cereal grains, threshers for separating the seed from the plant; whereas corn or maize harvesting is performed by employing a specially designed mechanical device ' mechanical corn pickers.' A typical harvesting machine comprises of a traveling part, a reaping part, and a baler part.</p>

<p>Harvesting machines are also used for controlling the production of weeds. Machines like field choppers, balers, mowers, crushers and windrowers are the common examples of this category. A forage harvester is used for cutting and chopping of almost all silage crops.</p>
Types of Harvesting Machinery
Following is a brief description of major harvesting machines used all around the globe:

<ol><li>Crop Harvesting Machine:&nbsp;&nbsp; The mechanical device which harvests forage crops cultivated in upland/paddy field and forms roll bale simultaneously was developed, is termed as crop harvesting machinery. It comprises of traveling, reaping and a baler part.</li>
<li>&nbsp;Grain Harvesting Machine: ;&nbsp;&nbsp;This machine is used to harvest grains, the edible brans or fruit seeds of a cereal crop.</li>
<li>Rootcrop Harvesting Machine:&nbsp;&nbsp; Traditionally root crops are harvested with diggers and digger-pickers. Now a days, several machines are available in the market. Modern sugar-beet harvester is one of the most popular examples of the root crop harvesting machine.</li>
<li>&nbsp;Threashers: &nbsp;&nbsp;First invented by the Scottish mechanical engineer Andrew Meikle, threshers or threshing machine is used for the separation of grain from stalks and husks.</li>
<li>&nbsp;Vegetable Harvesting Machine: &nbsp;&nbsp;Nowadays, machines are also available for the harvesting of vegetables. These 'vegetable harvesting machines', are quite common among the global vegetable farmers. Tomato harvesting machine is the most common example of this.</li></ol>.
 

     
